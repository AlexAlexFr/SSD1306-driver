void SSD1306_Init()
{
	i2c_Init();
	
	SSD1306_SendCommand(SSD1306_SCROLL_OFF);
	
	SSD1306_SendCommand(SSD1306_SET_MUX);
	SSD1306_SendCommand(0x3F);
	
	SSD1306_SendCommand(SSD1306_DISPLAY_OFFSET);
	SSD1306_SendCommand(0x00);
	
	SSD1306_SendCommand(SSD1306_DISPLAY_SL);
	
	SSD1306_SendCommand(SSD1306_INV_SEG);
	
	SSD1306_SendCommand(SSD1306_INV_COM);
	
	SSD1306_SendCommand(SSD1306_COM_PIN_CONF);
	SSD1306_SendCommand(SSD1306_NORMAL_PIN|SSD1306_ALTERNATIVE_PIN);
	
	SSD1306_SendCommand(SSD1306_SET_CONTRAST);
	SSD1306_SendCommand(0x7F);
	
	SSD1306_SendCommand(SSD1306_ALL_DISP_ON);
	
	SSD1306_SendCommand(SSD1306_DISPLAY_RESET);
	
	SSD1306_SendCommand(SSD1306_DISPLAY_CLK);
	SSD1306_SendCommand(0x80);
	
	SSD1306_SendCommand(SSD1306_SET_PUMP);
	SSD1306_SendCommand(0x14);
	
	SSD1306_SendCommand(SSD1306_DISPLAY_ON);
	
}

void SSD1306_SendCommand(char cCommand)
{
	i2c_Start();
	i2c_SendByte(SSD1306_WRADR);
	i2c_SendByte(0x00);
	i2c_SendByte(cCommand);
	i2c_Stop();	
}

void SSD1306_SetPages(char cPageStart, char cPageEnd)
{
		SSD1306_SendCommand(SSD1306_SET_PAGE);
		SSD1306_SendCommand(cPageStart & 0x07);
		SSD1306_SendCommand(cPageEnd & 0x07);
}

void SSD1306_SetColumns(char cColStart, char cColEnd)
{
	SSD1306_SendCommand(SSD1306_SET_COLUMN);
	SSD1306_SendCommand(cColStart & 0x7F);
	SSD1306_SendCommand(cColEnd & 0x7F);
}

void SSD1306_SetAdrMode(char cAdrMode)
{
		SSD1306_SendCommand(SSD1306_SET_ADR_MODE);
		SSD1306_SendCommand(cAdrMode & 0x03);
}

void SSD1306_SendBuffer(char* cBuffer, int iBufferLen)
{

		i2c_Start();
		i2c_SendByte(SSD1306_WRADR);
		i2c_SendByte(SSD1306_DATA_TRANSMIT);
		
		for (int i=0; i<iBufferLen; i++)
		{
			i2c_SendByte(cBuffer[i]);
		}
		
		i2c_Stop();
	
}

void SSD1306_ClearDisplay()
{
	SSD1306_SetAdrMode(SSD1306_HORZ_MODE);
	SSD1306_SetColumns(0,SSD1306_H_RES-1);
	SSD1306_SetPages(0,SSD1306_PAGES_NB);
	
	i2c_Start();
	i2c_SendByte(SSD1306_WRADR);
	i2c_SendByte(SSD1306_DATA_TRANSMIT);
		
	for (int i=0; i<SSD1306_H_RES*SSD1306_PAGES_NB; i++)
	{
		i2c_SendByte(0x00);
	}
		
	i2c_Stop();
	
}

void SSD1306_FillDisplay(char cByte)
{
	SSD1306_SetAdrMode(SSD1306_HORZ_MODE);
	SSD1306_SetColumns(0, 0x7F);
	SSD1306_SetPages(0,SSD1306_PAGES_NB);
	
	char cBuf[SSD1306_H_RES*SSD1306_PAGES_NB];
	
	for (int iCount = 0; iCount<SSD1306_H_RES*SSD1306_PAGES_NB;iCount++)
	{
		cBuf[iCount] = cByte;
	}

	SSD1306_SendBuffer(cBuf,SSD1306_H_RES*SSD1306_PAGES_NB);
}

void SSD1306_WriteText(char* cText, char cTextLen)
{
	char cConvText[cTextLen*FONT5x8_WIDTH];
	for (char cCount=0; cCount<cTextLen; cCount++)
	{
		for(char cLetter=0; cLetter<FONT5x8_WIDTH; cLetter++)
		{
			cConvText[cCount*FONT5x8_WIDTH + cLetter] = pgm_read_byte(&cFont5x8[cText[cCount]*FONT5x8_WIDTH + cLetter]);
		}
	}
	
	SSD1306_SendBuffer(cConvText, cTextLen*FONT5x8_WIDTH);
}

void SSD1306_WriteChar(char cChar)
{
	i2c_Start();
	i2c_SendByte(SSD1306_WRADR);
	i2c_SendByte(SSD1306_DATA_TRANSMIT);
	for (char c=0;c<FONT5x8_WIDTH;c++)
	{
		i2c_SendByte( pgm_read_byte(&cFont5x8[cChar*FONT5x8_WIDTH + c]));
	}
	i2c_Stop();
}

void SSD1306_DrawRectIcon(char* cIcon, char sColumn, char sPage, char cIconWidth, char cIconHeight)
{
	SSD1306_SetColumns(sColumn, sColumn + cIconWidth - 1);
	SSD1306_SetPages(sPage, sPage + cIconHeight / 8 - 1);


	i2c_Start();
	i2c_SendByte(SSD1306_WRADR);
	i2c_SendByte(SSD1306_DATA_TRANSMIT);
	for (char c=0;c<32 ;c++)
	{
		i2c_SendByte(pgm_read_byte(&cIcon[c]));
	}
	i2c_Stop();

}

void SSD1306_DisplayOnoff(char cDispOnOff)
{
	SSD1306_SendCommand(SSD1306_DISPLAY_OFF|(cDispOnOff & 0x01));
}

void SSD1306_SetContrast(char cContrast)
{
		SSD1306_SendCommand(SSD1306_SET_CONTRAST);
		SSD1306_SendCommand(cContrast);
}

void SSD1306_ScrollOnOff(char cScrollOnOff)
{
	SSD1306_SendCommand(SSD1306_SCROLL_OFF|(cScrollOnOff & 0x01));
}

void SSD1306_HorizontalScroll(char cDir, char cStartPage, char cInterval, char cEndPage)
{
	SSD1306_ScrollOnOff(SSD1306_OFF);
	SSD1306_SendCommand(SSD1306_SCROLL_RIGHT|(cDir & 0x01));
	SSD1306_SendCommand(0x00);
	SSD1306_SendCommand(cStartPage & 0x07);
	SSD1306_SendCommand(cInterval & 0x07);
	SSD1306_SendCommand(cEndPage & 0x07);
	SSD1306_SendCommand(0x00);
	SSD1306_SendCommand(0xFF);
	SSD1306_ScrollOnOff(SSD1306_ON);
}